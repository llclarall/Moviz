// Fonction pour charger filmsData depuis un fichier JSON
function loadFilmsData(file) {
    return fetch(file)
        .then(response => response.json())
        .catch(error => {
            console.error("Erreur lors du chargement des données :", error);
            throw error;
        });
}

// Fonction pour enrichir chaque film avec poster et bande-annonce
function fetchFilmPosterAndTrailer(film) {
    const allocineId = idAlloCine[film.imdb];
    if (!allocineId) {
        console.log(`Aucun ID Allociné trouvé pour ${film.imdb}`);
        return Promise.resolve(); // Ignorer ce film si pas d'ID Allociné
    }

    const url = `https://igm.univ-mlv.fr/~gambette/ENSIUT/allocine/AllocineAPI-main/AllocineAPI-main/index.php?id=${allocineId}`;

    return fetch(url)
        .then(response => response.json())
        .then(data => {
            if (data.error === undefined) {
                film.poster = data.urlAffiche;
                film.trailer = data.urlBandeAnnonce;
                console.log(`Poster et bande-annonce ajoutés pour ${film.imdb}`);
            } else {
                console.log(`Erreur pour l'ID ${film.imdb} : ${data.error}`);
            }
        })
        .catch(error => console.error(`Erreur pour ${film.imdb} :`, error));
}

// Fonction principale pour enrichir les données
function getPostersAndTrailers(filmsData) {
    let index = 0;

    function processNextFilm() {
        if (index < filmsData.length) {
            const film = filmsData[index];
            index++;
            fetchFilmPosterAndTrailer(film).then(processNextFilm);
        } else {
            console.log("Tous les posters et bandes-annonces ont été récupérés.");
            console.log(filmsData);

            // Optionnel : sauvegarder ou télécharger les données enrichies
            saveEnrichedData(filmsData);
        }
    }

    processNextFilm(); // Lancer le traitement
}

// Fonction pour sauvegarder les données enrichies localement (le télécharger)
function saveEnrichedData(filmsData) {
    const jsonData = JSON.stringify(filmsData, null, 2);
    const blob = new Blob([jsonData], { type: "application/json" });
    const url = URL.createObjectURL(blob);

    const a = document.createElement("a");
    a.href = url;
    a.download = "filmsDataEnriched.json";
    a.click();

    console.log("Données enrichies prêtes à être téléchargées.");
}



